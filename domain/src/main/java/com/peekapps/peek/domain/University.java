public class University {
    private String id;
    private String name;
    private double latitude;
    private double longitude;
    private String city;
    private String type;
    private String address;

    // User specific attrs - maybe better in presentation layer as separate entity?
    private double distanceFromUser;
    private int minutesAgoUpdated;
    private int numberOfPhotos;

//    private String country;
//    private boolean withinBounds;

    public University(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public double getDistanceFromUser() {
        return distanceFromUser;
    }

    public void setDistanceFromUser(double distanceFromUser) {
        this.distanceFromUser = distanceFromUser;
    }

    public int getMinutesAgoUpdated() {
        return minutesAgoUpdated;
    }

    public void setMinutesAgoUpdated(int minutesAgoUpdated) {
        this.minutesAgoUpdated = minutesAgoUpdated;
    }

    public int getNumberOfPhotos() {
        return numberOfPhotos;
    }

    public void setNumberOfPhotos(int numberOfPhotos) {
        this.numberOfPhotos = numberOfPhotos;
    }
}
